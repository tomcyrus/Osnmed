            <p>Women are facing many gynecological problems in their lives. One of them is endometriosis that is becoming common. It is considered as a painful disorder in which the endometrium grows outside the uterus. Endometriosis involves ovaries, tissue lining of the pelvis, & fallopian tubes in women. In endometriosis, the endometrial tissue gets thicken, bleed, & breakdown with each menstrual cycle.
                As these displaced tissues have no exit it gets trapped in the body. Finally, endometriomas are formed when endometriosis involves cysts & ovaries.</p>
            <h3 class="mb-4">What are the Signs and Symptoms of Endometriosis?</h3>

            <p><b>The signs and symptom that are common in an endometriosis patient are:</b></p>
                <ul class="list-styled mb-5">
                    <li>Pelvic pain and cramps</li>
                    <li>Painful periods</li>
                    <li> Lower backache</li>
                    <li> Vomiting</li>
                    <li> Infertility</li>
                    <li>Nausea</li>
                    <li> Bloating</li>
                    <li> Fatigue</li>
                    <li> Bleeding or spotting between periods</li>
                </ul>

            <h3 class="mb-4">What are the Causes of Endometriosis?</h3>
            <p>The exact cause of endometriosis is exactly not known. There are different possible causes for endometriosis mentioned below in detail:</p>

            <ul class="list-styled mb-5">
                    <li>Estrogen hormones transform embryonic cells that implant endometrial cells during puberty.</li>
                    <li>In retrograde menstruation, the blood that contains endometrial cells flow back through fallopian tubes and into the pelvic cavity. These endometrial cells get attached to the pelvic walls and surface of pelvis organs.</li>
                    <li> The tissue fluid or blood vessels transport endometrial cells to other body parts.</li>
                    <li> A weak immune system destroys the endometrial tissue that is growing outside the uterus.</li>
                </ul>

