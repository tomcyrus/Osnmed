            <p>Coeliac disease causes weak immune system so is considered as auto-immune disease. In this disease the body’s immune system become so weak that it mistakenly attack healthy tissues in the body. It slowly damages the small intestine & the body’s ability to absorb nutrients get disturbed day to day. Sometimes hereditary factors also play a major role in coeliac disease.</p>

            <h3 class="mb-4">What are the complications of Coeliac disease?</h3>
            <p>If coeliac disease is left untreated than it created long-term complications such as iron deficiency in the body (anemia), osteoporosis (weakening of the bones), folate deficiency & vitamin B12 deficiency.</p>

