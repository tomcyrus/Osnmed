            <p>The human brain is awesome as it functions twenty-four hours a day from the day we are born.
                It is considered as one of the important organ in our body. The brain power improves by the use of it. Nowadays there are many neuro-degenerative diseases that affect the brain.</p>

                <p>One of the diseases that come under neurodegenerative disease is Alzheimer’s that is the most common type of dementia. It is a progressive disease that begins with mild memory loss & involves the parts of the brain that controls memory, language, and thought.
                In this disease, the death of brain cells takes place that causes loss of memory. After the age of 60, the symptoms of this disease appear & risks of this disease increase with the age.</p>

                <p>The progression of this disease is done on three main stages before the symptoms appear (preclinical), when symptoms are mild (mild cognitive impairment), & dementia.</p>
            <h3 class="mb-4">What are the Symptoms of Alzheimer’s Disease?</h3>

            <p>The early signs of this disease are loss of memory as the disease progress the memory impairment worsens. The different symptoms are mentioned below:</p>
            <ul class="list-styled mb-5">
           <li> <b>Memory loss:</b> People with Alzheimer’s disease forget the conversation, events, or appointments, Repeat questions and statements, forget the name of family members, & they have trouble to identify objects,take part in conversations, or express thoughts.</li>
           <li> <b>Difficulty in making decisions & judgments:</b> The ability to make proper judgments & reasonable decisions become worse. For example, it becomes more difficult for an affected person to do everyday workings like an unexpected driving situation.</li>
           <li> <b>Changes seen in the behavior & personality:</b> A sufferer of Alzheimer’s disease faces change in behaviors, mood swings, depression, social withdrawal, irritability, loss of inhibitions, sleep habits change, aggressiveness, delusions, etc.</li>
           <li> <b>The thinking & reasoning pattern changes:</b>  This disease causes difficulty in thinking & concentrating. A person may feel difficulty in recognizing and dealing with numbers.</li>
           <li> <b>Difficulty in performing and planning familiar tasks:</b>Patient of Alzheimer’s disease faces difficulty in doing routine activities like cooking meal, playing a favorite game, doing basic tasks such as bathing and dressing.</li>
            </ul>

            <h3 class="mb-4">What are the Causes of Alzheimer’s Disease?</h3>
            <p>Most of the people believe that Alzheimer’s disease is caused due to a combination of lifestyle, genetic, & environmental factors. The part of the brain that controls memory is damaged in this condition.
                In the late stage, the brain shrunk. Loss of neurons that further spread to the other parts of the brain.</p>

                <h3 class="mb-4">What are the Risk Factors of Alzheimer’s Disease?</h3>   
            <ul class="list-styled mb-5">

            <li>This disease is more common in people at the age of 60’s.</li>
            <li>The genetic factor, if your parent or sibling is affected with this disease than you might be a sufferer.</li>
            <li>Women are more prone to this disease than men.</li>
            <li>People with Down syndrome may develop Alzheimer’s disease.</li>
           <li>The patient with Alzheimer’s disease faces difficulty in sleeping.</li>
            <li>Past head trauma patients are at higher of Alzheimer’s disease.</li>
            <li>There is mild cognitive impairment in thinking skills and memory.</li>
            <li>Lifestyle problems like lack of exercise, high blood pressure, diabetes, lack of exercise, high cholesterol, etc.</li>
            </ul>

