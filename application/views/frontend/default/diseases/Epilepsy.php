            <p>Epilepsy is a medical condition that affects the central nervous system. In other words, it is also known as a Seizure disorder. In this nerve cell activity in the brain gets disturbed. Anyone can develop epilepsy, but it’s more common in young children and older adults. It occurs slightly more common in males than in females. Epilepsy affects more than 50 million people worldwide.</p>
            <h3 class="mb-4">What are the Types of Epilepsy?</h3>

            <p><b>There are mainly two types of Epilepsy (Seizure) </b></p>
                <ul class="list-styled mb-5">
                    <li>Focal Seizure</li>
                    <li>Generalized Seizure</li>
                </ul>

            <h3 class="mb-4">1. &nbsp;  Focal Seizure</h3>
            <p>When seizure appears to result from abnormal activity in just one area of the brain. These seizures fall into two categories</p>
            <ul class="list-styled mb-5">
                    <li><b>Focal seizure without loss of consciousness –</b>They may alter emotions or change the way things like look, smell, feel, taste or sound. A Focal seizure may also result in involuntary jerking of a body part, such as an arm or leg.</li>
                    <li><b>Focal seizure with impaired awareness–</b>The seizure involves a change or loss of awareness. In this, you may not respond normally to your environment or perform repetitive movements, such as hand rubbing, chewing, swallowing, etc</li>
            </ul>

            <h3 class="mb-4">2. &nbsp; Generalized Seizure</h3>
            <p>Seizure that appears to involve all areas of the brain. They are of six types</p>
            <ul class="list-styled mb-5">
                    <li><b>Absence seizure –</b>They are characterized by subtle body movements such as eye blinking or lip smacking. The seizure may cause loss of consciousness.</li>
                    <li><b>Tonic seizure–</b> It mainly causes stiffening in the muscles. Tonic seizure mostly affects the muscles of arms, legs, back, and may cause you to fall on the ground.</li>
                    <li><b>Atonic seizure-</b> It causes a loss of muscle control, which may cause you to suddenly collapse or fall down.</li>
                    <li><b>Clonic seizure–</b> They are associated with repeated or rhythmic, jerking muscle movements. It usually affects the neck, face, and arms.</li>
                    <li><b>Myoclonic seizure-</b>It usually appears as sudden brief jerks or twitches of your arms and legs.</li>
                    <li><b>Tonic-Clonic seizure –</b> This is the most dramatic type of seizures, which cause an abrupt loss of consciousness, body stiffening, and shaking. Sometimes loss of bladder control can occur.</li>
            </ul>

            <h3 class="mb-4">What are the Signs and Symptoms of Epilepsy?</h3>
            <p>Epilepsy is caused by abnormal activity in the brain; a seizure can affect any process which your brain coordinates. Signs and Symptoms include-</p>
            <ul class="list-styled mb-5">
                    <li>Temporary confusion</li>
                    <li>Changes in smell, touch, and sound</li>
                    <li>Psychic symptoms such as fear, anxiety, & stress</li>
                    <li>Jerking movements of the arms and legs that are not in control of an individual</li>
                    <li>Loss of consciousness</li>
                    <li>Intermittent fainting spells, in which bowel or bladder control is lost</li>
                    <li>Biting of the tongue</li>
                </ul>
            
                <h3 class="mb-4">What are the Causes of Epilepsy?</h3>
                <p>Epilepsy is caused by abnormal activity in the brain; a seizure can affect any process which your brain coordinates. Signs and Symptoms include-</p>
                <ul class="list-styled mb-5">
                        <li>Genetics – Genetics is believed to be involved in the majority of cases, either directly or indirectly.</li>
                        <li>Acquired – Epilepsy may occur a number of other conditions including tumors, strokes, head trauma, previous infection of the Central nervous system.</li>
                        <li>Infectious diseases, such as meningitis, AIDS, and viral encephalitis.</li>
                        <li>Developmental disorders – Epilepsy can sometimes be associated with autism and neurofibromatosis.</li>
                        <li>Loss of consciousness</li>
                        <li>Prenatal injury- Before birth, babies are sensitive to brain damage that is caused by an infection in the mother, poor nutrition.</li>
                        <li>Head trauma as a result of car accidents or other traumatic injuries.</li>
                    </ul>


            <h3 class="mb-4">What are the Risk Factors of Epilepsy?</h3>
            <p><b>Risk factors may include –</b></p>
            <ul class="list-styled mb-5">
                <li>Age – Most common in children and older adults.</li>
                <li>Dementia</li>
                <li>Family history</li>
                <li>Brain infections – Infection such as meningitis, which causes inflammation in your brain or spinal cord.</li>
                <li>Seizure in childhood</li>
            </ul>

