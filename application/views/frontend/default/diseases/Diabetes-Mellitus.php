          <h3 class="mb-4">What is Diabetes Mellitus?</h3>
          <br>
          <p><b>Introduction –</b></p>
          <br>
            <p>It is a serious disease in which a person’s body cannot control the level of sugar in the blood. Diabetes Mellitus is a disorder of carbohydrate metabolism characterized by the impaired ability of the body to produce or responds to insulin. Thereby maintain the proper level of sugar (glucose) in the blood.
             Presently, environmental factors play a role in the etiopathogenesis of diabetes. Polluted air, soil, water, unhealthy diet, stress, lack of physical activity, vitamin –D deficiency, exposure to enteroviruses, and damage to the immune cells are responsible for Diabetes mellitus.</p>

            <h3 class="mb-4">What are the Different Types of Diabetes Mellitus?</h3>
            <p>Diabetes occurs either the cells of the body are not responding properly to the insulin produced or the pancreas not producing enough insulin, or the cells of the body not responding properly to the insulin produced. Types of diabetes are –</p>
            
            <ul class="list-styled mb-5">
                        <li><b>Type 1 diabetes</b> It results from the pancreas failure that produces enough insulin due to the loss of beta cells. This was previously known as ‘’insulin – dependent diabetes mellitus’’ (IDDM) or ‘’juvenile diabetes’’.</li>
                        <li><b> Type 2 diabetes :</b>It begins with insulin resistance, a condition in which cells fail to respond to insulin properly. This form was previously referred to as’ ‘insulin dependent diabetes mellitus’’ (NIDDM).</li>
                        <li><b>Gestational diabetes-</b> – It mostly occurs when pregnant women without a previous history of diabetes develop high blood sugar levels.</li>
            </ul>

            <h3 class="mb-4">What are the Signs and Symptoms of Diabetes Mellitus?</h3>
            <p>The signs & symptoms related to Diabetes mellitus patients are:</p>

                <ul class="list-styled mb-5">
                    <li>Frequent urination (Polyuria)</li>
                        <li>Disproportionate thirst (Polydyspia)</li>
                    <li> Intense hunger (Polyphagia)</li>
                    <li>Increased fatigue and irritability</li>
                    <li> Sexual dysfunction</li>
                    <li> Weight gain or unusual weight loss</li>
                    <li> Non – healing cuts and bruises</li>
                    <li> Increased fatigue and irritability</li>
                    <li> Certain medications, such as certain birth control pills or corticosteroids</li>
                </ul>

            <h3 class="mb-4">What are the Causes of Diabetes Mellitus?</h3>
            <ul class="list-styled mb-5">
                    <li>Excessive food intake which is difficult to digest (fried foods, creams, pizza, pasta, etc.</li>
                    <li>Mental stress and strain</li>
                    <li>Heredity</li>
                    <li>Lack of exercise</li>
                    <li> Excessive sleep</li>
                    <li>Overeating</li>
                    <li> Obesity</li>
                    <li> Overloading of fats and proteins</li>
                    <li> excessive intake of sugar and refined carbohydrates</li>
                </ul>

                <h3 class="mb-4">What are the Risk Factors of Diabetes Mellitus?</h3>
            <ul class="list-styled mb-5">
                    <li>Disease of pancreas</li>
                    <li>Infection or illness</li>
                    <li>Impaired glucose tolerance</li>
                    <li>Ethnic background</li>
                    <li> Age> 45</li>
                    <li>Sedentary lifestyle</li>
                    <li> Hypertension (B.P > 140/90 mm of Hg)</li>
                    <li> Polycystic ovarian syndrome.</li>
                    
                </ul>

