            <p>Amenorrhea is defined as the absence of a menstrual period in a woman during her reproductive age.  Women who missed her three menstrual periods are dealing with amenorrhea problem.
                It is a normal feature in prepubertal, pregnant, and post-menopausal females. The physiological state of amenorrhea is seen most commonly during pregnancy and lactation. Apart from the reproductive years, there is an absence of menses during childhood and after menopause.
                For a woman to have regular menstrual cycles, her hypothalamus  (stimulates the pituitary gland), pituitary gland (that releases luteinizing hormone and follicle stimulating hormone which influence the production of estrogen and progesterone that control cyclic changes in the lining of the uterus), ovaries, and uterus must be functioning properly.
                Environmental factors play an important role in determining the menstrual flow, its integrity, and regularity. Menstrual dysfunction reveals infertility and increases the future risk of various chronic diseases such as diabetes, breast cancer, and cardiovascular diseases.</p>
            
