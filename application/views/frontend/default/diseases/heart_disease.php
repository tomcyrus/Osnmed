            <p>Heart disease is a condition that affects your heart & mainly occurs due to the sedentary lifestyle of an individual. The heart diseases that are very common are (arrhythmias) heart rhythm problems, coronary artery disease, congenital heart defects (defect in heart by birth), dilated cardiomyopathy,  heart failure, myocardial infarction, mitral regurgitation, hypertrophic cardiomyopathy, pulmonary stenosis, mitral valve prolapse, pericardial disease, rheumatic heart disease, etc.</p>
            <p>The heart is the most important organ in our body as if it stops working then we get died. Heart diseases are also known as cardiovascular diseases that involve blocked or narrow blood vessels that cause heart attack, stroke, & pain in the chest region. Ayurveda provides the best & effective treatment for different heart diseases.</p>
            <h3 class="mb-4">What are the signs & symptoms seen in different types of heart diseases?</h3>

            <p><b>The different signs & symptoms according to the type of heart diseases are:</b></p>
               
            <p><b>1).&nbsp;Atherosclerotic disease: </b>The different signs & symptoms seen are chest pain, chest discomfort, pressure on the chest, shortness of breath, pain in neck, throat, jaw, upper abdomen, weakness, coldness, numbness, or pain in legs or arms if the blood vessels get narrowed in those body parts.</p>
            <p><b>2).&nbsp; Heart arrhythmia (Abnormal heartbeat): </b>In this condition, your heart may beat too slow, too quick, or irregularly. The common symptoms seen in these patients are the slow heartbeat, pain in chest, discomfort, dizziness, lightheadness, shortness of breath, racing heartbeat, fainting.</p>
            <p><b>3).&nbsp; Dilated cardiomyopathy: </b> The signs & symptoms seen in these patients are fatigue, swelling in legs, ankles, feet, breathlessness, irregular heartbeats, fainting, dizziness, lightheadedness, etc.</p>
            <p><b>4).&nbsp;Congenital heart disease: </b> It is the defect that you are born with & seen soon after birth. The signs & symptoms seen are blue or pale gray skin color, shortness of breath, swelling in the legs, abdomen, & around the eyes.</p>
            <p><b>5).&nbsp; Heart infections: </b>This affects the chambers & valves of the heart. The signs & symptoms seen are weakness, shortness of breath, fever, swelling in the abdomen or legs, changes in heart rhythm, dry cough, skin rashes or unusual spots.</p>
            <p><b>6).&nbsp;Valvular heart disease: </b>In this condition, valves get damaged leading to stenosis, prolapse, & regurgitation. The signs & symptoms seen are chest pain, irregular heartbeat, shortness of breath, fatigue, swollen feet or ankle, fainting, etc.</p>

            <h3 class="mb-4">What are the causes of heart diseases?</h3>
            <p>According to the type of heart diseases, there are different causes related:</p>
                <p><b>1).&nbsp;Atherosclerotic: </b> Atherosclerosis is the most common cause of heart disease. Being overweight, unhealthy diet, smoking, & lack of exercise is the reason for it.</p>
                <p><b>2).&nbsp; Heart arrhythmia: </b> Atherosclerosis is the most common cause of heart disease. Being overweight, unhealthy diet, smoking, & lack of exercise is the reason for it.</p>
                <p><b>3).&nbsp; Dilated cardiomyopathy: </b> It is caused by reduced blood flow to the heart that results in damage after a heart attack, toxins, infections, & certain drugs.</p>
                <p><b>4).&nbsp; Restrictive cardiomyopathy: </b> It is the least common cardiomyopathy that may cause hemochromatosis, amyloidosis, & other connective tissue disorders.</p>
                <p><b>5).&nbsp;Congenital heart disease: </b> In this type, the heart muscles become abnormal. It develops due to high blood pressure or aging.</p>
                <p><b>6).&nbsp; Heart infections: </b> The bacteria, parasites, & viruses are responsible for the development of heart infections.</p>
                <p><b>7).&nbsp;Valvular heart disease: </b>The different causes of heart valvular disease are infections, connective tissue disorders, & rheumatic fever.</p>

            <h3 class="mb-4">What are the risk factors of heart diseases?</h3>
            <p>There are different risk factors that are responsible for heart diseases:</p>
                <ul class="list-styled mb-5">
                    <li><b>Age:</b> As the age increases there is a high risk of damaged & narrowed arteries that ultimately causes heart diseases.</li>
                    <li><b>Family history:</b>In your family, if anyone is having history of heart disease than you may have this disease.</li>
                    <li><b>Sex:</b> Men are at higher risk of developing heart disease as compare to women.</li>
                    <li><b>Unhealthy diet:</b>Consumption of extra fat, sugar, salt, & cholesterol increases the risk of heart disease.</li>
                    <li><b>High cholesterol levels:</b> Increased cholesterol levels in the blood forms plaques & atherosclerosis.</li>
                    <li><b>High blood pressure:</b>High blood pressure results in thickening & hardening of arteries that in the end develop heart disease.</li>
                    <li><b>Obesity:</b>Obese people are at a higher risk.</li>
                    <li><b>Diabetes:</b>Diabetes a life-style disorder increases the risk of heart disease.</li>                        
                </ul>

