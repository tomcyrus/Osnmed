            <p>Azoospermia is a condition when there is absence of sperm in males.
                In this situation, there is no sperm found in the fluid on a routine analysis of semen. It is usually a unpleasant surprise for males, which leads to infertility.
                 This condition is seen in 1% of all males & 15% of infertile males.</p>

