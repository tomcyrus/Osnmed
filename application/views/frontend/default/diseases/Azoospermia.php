            <h3 class="mb-4">What are the two types of Azoospermia & its causes?</h3>

            <p>Azoospermia is divided into two categories:</p>
            <p>1) Obstructive Azoospermia and &nbsp; 2) Non-Obstructive Azoospermia</p>

            <p><b>1) &nbsp;Obstructive Azoospermia:</b>In obstructive azoospermia, sperms are produced inside the testicles.
            Obstruction or blockage is seen in the reproductive tubing that prevents the sperm to flow outside the world.
            The causes of obstructive azoospermia can be congenital, genetic, or acquired .Other causes are:</p>

            <p>a) &nbsp;vasectomy a surgical procedure that interrupts the sperm ducts to stop the flow of sperm.
            b) &nbsp;Congenital conditions: In this situation, some men are born with missing vas deferens other men are born with cyst in prostate that creates a blockage in ejaculatory ducts.
            c)&nbsp; Infections: In this condition, infections of the testicles, prostate, or reproductive tract like epididymitis, prostatitis, orchitis, & venereal diseases like chlamydia take place. It can cause a blockage of ejaculatory duct or small tubules of the epididymis.
            d) &nbsp;Surgical complications: The procedures that are performed during surgery on the urogenital organs or hernia repair cause scarring that results to blockage.
            </p>

            <p><b>2) &nbsp;Non-Obstructive Azoospermia:</b>Non-Obstructive Azoospermia is a condition, in which the tubes are open, there is no production or low level of sperm production. This level is too much low that the sperm is not able to make the way out of the testicle.
                    The ca uses of non-obstructive azoospermia is abnormalities with the reproductive hormones or the testicles that control production of sperm. It can be congenital, genetic, or acquired.</p>

            <p><b>a)&nbsp; Genetic causes: </b> The most common chromosomal problem that causes azoospermia in male is when there is an extra X chromosome (Klinefelter Syndrome).
                         Another causes of azoospermia is deletion of Y-chromosome that is responsible for male characteristics.
                     When one  or more  genes are deleted in the chromosomes it results in azoospermia. Most of the genetic issues produce small amounts of sperm within the testicles.</p>
            
            <p><b>b)&nbsp; Hypospermatogenesis: </b>This situation is found through routine semen analysis in which sperm production within the testicle are just at lower amounts than normal.</p>

            <p><b>c)&nbsp; Varicocele:</b> It is characterized by varicose veins around the testicle that causes male infertility.</p>

            <p><b> d) &nbsp;Maturation arrest:</b>It can occur at early or late stage of sperm maturation process.</p>
            <p><b> e) &nbsp; Testicular cancer: :</b>Testicular cancer or other tumors of the reproductive system is responsible for azoospermia or very low sperm counts.</p>

