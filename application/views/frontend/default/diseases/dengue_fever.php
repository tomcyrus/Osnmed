            <p>When we heard about dengue fever it makes us scary as we lose whole energy and have a high-grade fever. Dengue fever is transmitted by a mosquito named as Aedes mosquito. It is a viral fever that is related to the viruses that cause yellow fever.
            This fever is most common in Western Pacific Island & Southeast Asia. Dengue fever can affect anyone but is more severe in people with compromised immunity.</p>

            <p>This fever can be named with other names like dandy fever or breakbone fever. If you are having mild dengue fever it causes rash, muscle and joint pain, high fever. Severe dengue fever causes dengue hemorrhagic fever that causes a drop in blood pressure, severe bleeding, & sometimes death.</p>
            <h3 class="mb-4">What are the Signs and Symptoms of Dengue Fever?</h3>

            <p>In mild dengue fever, most of the people may experience no signs or symptoms. But these may begin in four to seven days after bitten by an infected mosquito:</p>
            <ul class="list-styled mb-5">
           <li> High-grade fever 104 F degree</li>
            <li>Headache</li>
           <li> Nausea</li>
           <li> Vomiting</li>
           <li> Rashes</li>
           <li> Muscle pain</li>
           <li> Bone & joint pain</li>
           <li> Pain behind the eyes</li>
           <li> Rashes</li>
            </ul>
            <p><b>In severe dengue, the signs & symptoms include:</b></p>

            <ul class="list-styled mb-5">
           <li> Bleeding from nose or gums</li>
            <li>Blood in stools, urine, or vomit</li>
           <li> Difficulty in breathing</li>
           <li> General weakness</li>
           <li> Persistent vomiting</li>
           <li> Restlessness</li>
           <li> Bone & joint pain</li>
           <li> Severe headache</li>
           <li> Platelet count decreases</li>
           <li>Fatigue</li>
           <li>Abdominal pain</li>
            </ul>

            <h3 class="mb-4">What are the Causes of Dengue fever?</h3>
            <p>A mosquito bite is the main reason for dengue fever. When a mosquito bites an infected person with dengue virus, this virus enters the mosquito.
            After that when this infected mosquito bites another person the virus enters the bloodstream of that person.</p>

            <h3 class="mb-4">What are the Risk Factors of Dengue Fever?</h3>
            <p>The risk factors related to dengue fever are:</p>

            <ul class="list-styled mb-5">
                <li>Being in tropical and subtropical areas increases the risk of dengue fever. As these mosquitoes are found in this </li>
                <li>Previous infection with dengue fever increases the risk of this disease.</li>
            </ul>

