            <h3 class="mb-4">What are the Causes of IBS?</h3>
            <p>Although the exact cause of IBS is unknown. Possible causes include an overly sensitive colon or immune system. Post-infection is caused by a bacterial infection in the gastrointestinal tract.</p>
            <p>The physical processes involved in IBS can also vary, mainly consists of</p>
            <ul class="list-styled mb-5">
                <li>Painful cramping (slow and spastic movements of the colon</li>
                <li>Abnormal serotonin levels in the colon, affecting bowel movements and motility.</li>
                <li>Mild celiac disease that damages the intestine, causing IBS symptoms.</li>
            </ul>

