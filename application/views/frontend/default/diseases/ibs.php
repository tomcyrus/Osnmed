            <p>IBS (Irritable bowel syndrome) also called as irritable colon or spastic colon, mucous colitis. It is associated with significant health care and economic burden. IBS can also severely compromise a person’s quality of life. IBS is best understood as a long term recurrent disorder of gastrointestinal functioning. It usually involves the large intestine and small intestine. This inconvenience can produce symptoms of abdominal pain, bloating, gastritis, and a change in a bowel routine.</p>
