            <h3 class="mb-4">What are the causes of food poisoning?</h3>
            <p>The different causes of food poisoning are mentioned below in detail:</p>
            <ul class="list-styled mb-5">
                    <li><b>1) &nbsp; Viruses: </b> When food poisoning is caused due to virus that is transmitted through food. The different viruses such as norovirus, sapovirus, rotavirus, & astrovirus are responsible for it.</li>
                    <li><b>2) &nbsp; Bacteria: </b> The most common dangerous bacteria are E.coli, Salmonella, Listeria, & Campylobacter. These bacteria cause severe food poisoning in the patients.</li>
                    <li><b>3) &nbsp; Parasites: </b> Toxoplasma is the parasite that is commonly responsible for food poisoning. It makes your immune system weak.</li>
            </ul>

