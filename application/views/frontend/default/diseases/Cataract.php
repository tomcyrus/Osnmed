            <p>When there is inflammation of the airways that carry air to your lungs this condition is known as Bronchitis.This inflammation of the bronchial tubes causes cough with mucus, low-grade fever, shortness of breath, wheezing.
            If bronchitis becomes chronic than it leads to COPD (Chronic obstructive pulmonary disease).</p>

            <p>Excess cigarette smoking is the main cause of bronchitis. Exposure to air pollution, inhalation of dust for a longer period also leads to this disease.</p>
            <h3 class="mb-4">What are the sign and symptoms of Bronchitis?</h3>

            <p>The common signs & symptoms associated with bronchitis are mentioned below in details:</p>
            <ul class="list-styled mb-5">
           <li> Cough with mucus</li>
            <li>Chest congestion</li>
           <li> Shortness of breath</li>
           <li> Wheezing</li>
           <li> Low-grade fever</li>
           <li> Running nose</li>
           <li> Sore throat</li>
           <li> Bodyache</li>
           <li> Headache</li>
            </ul>

            <h3 class="mb-4">What are the causes of Bronchitis?</h3>
            <p>The different causes of Bronchitis are mentioned below in detail:</p>
            <ul class="list-styled mb-5">

            <li>Exposure to air pollution</li>
            <li>Inhalation of dust</li>
            <li>Excess smoking</li>
            <li>Bacterial & viral infection</li>
            </ul>

            <h3 class="mb-4">How many types of bronchitis are there?</h3>
            <p>Bronchitis mainly is divided into two types:-</p>
            <ul class="list-styled mb-5">

            <li><b>Acute Bronchitis -</b>Acute bronchitis is inflammation of the bronchial tube lasting for a short period of time.
            It is the most common type of bronchitis. In this type, symptoms are similar to cold like sore throat, headache, fatigue, bodyache, etc.</li>
            <li><b>Chronic Bronchitis –</b> In this type, symptoms are present from longer duration.
            Other symptoms of chronic bronchitis include cough with mucus, wheezing sounds, shortness of breath, feeling tired, etc.</li>
            </ul>

