            <p>There are different types of arthritis one among them is gout that is considered a complex form of arthritis. This disease is more common in men as compared to women. Women after menopause are more susceptible to gout. The patient dealing with cardiovascular disorders, hypertension, & obesity are at higher risk of facing gout problem. The formation of uric crystals in the joints is responsible for gout. It is caused by the deposition of excess uric acid in the bloodstream. In this disease, the base of the big toe is affected. At the middle of the night time the attack of gout increases. Gout is characterized by swelling, redness, tenderness in the joints, & severe attacks of pain.</p>
            <h3 class="mb-4">What are the Signs & Symptoms of Gout?</h3>

            <p><b>The patient having gout problem have signs & symptoms that usually appear at night time.</b></p>
                <ul class="list-styled mb-5">
                    <li>Intense pain in joints that usually affect the big toe, elbows, wrists, ankles, knees & fingers.</li>
                    <li>Redness, swelling, inflammation, tenderness, & stiffness in the joints.</li>
                    <li>With the progression of gout the joints are not able to move joints normally.</li>
                </ul>

            <h3 class="mb-4">What are the Causes of Gout?</h3>
            <p>Gout usually takes place when there is an accumulation of urate crystals in the joints. These crystals are formed when the level of uric acid increases in the blood. Uric acid is produced in the body when the purines are breakdown. These purines are mostly found in seafood, organ meat, & steak. The food items that promote a high level of uric acid are beer, alcoholic beverages, & sweetened drinks.</p>
            
            <h3 class="mb-4">What are the Different Types of Gout?</h3>
            <p>The various types of gout are mentioned below in detail:</p>
            
            <ul class="list-styled mb-5">
                    <li><b>1) &nbsp; Acute gout:  </b> In this type, the urate crystals are deposit suddenly causing intense pain, inflammation, & stiffness. These symptoms may subside within 3 to 10 days. The flares are triggered due to alcohol, drugs, & stressful events.</li>
                    <li><b>2) &nbsp; Intercritical gout:</b>In this type, the flares of a gout attack may not occur for months or years if not treated on time. If not treated over time these urate crystals may be deposited in tissues.</li>
                    <li><b>3) &nbsp; Asymptomatic hyperuricemia: </b> At this stage, the uric acid of a person is elevated without any symptoms. Urate crystals may be deposited in tissue that causes damage.</li>
                    <li><b>4) &nbsp; Pseudogout:</b> In pseudogout, the joints are irritated with calcium pyrophosphate crystals than urate crystals.</li>
                    <li><b>5) &nbsp; Asymptomatic hyperuricemia: </b> It is the most debilitating type of gout that permanently damages the joints & kidneys.</li>
            </ul>

            <h3 class="mb-4">Who are at a Higher Risk of Developing Gout?</h3>
            <p>There are many factors that are responsible for the development of gout such as:</p>
            
            <ul class="list-styled mb-5">
                    <li> The obese persons are at risk of developing more uric acid due to which kidneys have difficulty in developing uric acid.</li>
                    <li>Eating a diet rich in seafood, meat, & drinking beer, alcohol, soft drinks that are responsible for increasing gout risk.</li>
                    <li>Diseases such as high blood pressure, diabetes, heart diseases, metabolic syndrome, & kidney diseases are responsible for gout.</li>
                    <li>Family history is also another reason for the development of gout.</li>
                    <li>The patient between the ages of 30 & 50 is more likely to develop gout.</li>
                    <li>Trauma & recent surgeries can attack gout in an individual.</li>
            </ul>

