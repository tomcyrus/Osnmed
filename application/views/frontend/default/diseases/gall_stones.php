            <p>The Gallbladder is a very small organ that lies underneath the surface of the liver. Bile is secreted by the liver and stored in gallbladder. Then gall bladder contracts due to which bile is released into the intestine. When the gall bladder contracts it increases the number of hormone levels. Some of the chemicals exist in the gall bladder can solidify into either one large stone or several small ones.</p>
            <p>Gall stones are also known as Cholelithiasis. It is commonly referred to the condition where the stones are either in the gallbladder or in common bile duct. Stone can block the normal flow of bile if they lodge in any of the ducts that carries bile from liver to the small intestine. That may include, hepatic duct, cystic duct, common bile duct.</p>
            <p>The age range for the cases was between 15-85 years. The majority of the patients were in the range of 31 to 60 years. In India, the prevalence of gallstones ranges from 6% to 9% in the adult population. Age, Gender, Ethnicity diet and sedentary lifestyles influence the prevalence of gallstone.</p>
            
            <h3 class="mb-4">How many types of gallstone?</h3>
            <p>Major types of gallstones are as follow –</p>
            
            <ul class="list-styled mb-5">
                        <li><b>Cholesterol gallstones – </b>  They are composed mainly of cholesterol which is made in the liver and the most common kind which is approx. of 80% and is usually in yellow-green color.</li>
                        <li><b>Pigment gallstones -</b> These gallstones are composed of calcium salts, bilirubin and other materials. Color is usually dark brown or black in color.</li>
                        <li><b>Mixed stones -</b> – They are basically cholesterol stones which have been acted upon by the body’s bacteria and immune cells to break down the bilirubin. This causes a disturbance in the chemical composition of the stones where they begin to resemble that of pigment stones to some extent.</li>
            </ul>

            <h3 class="mb-4">What are the Risk factors of Gallstones?</h3>

            <ul class="list-styled mb-5">
                    <li>Gender – Females are at more risk of developing gallstones.</li>
                    <li>Weight – Obese people are at higher risk for developing this disease.</li>
                    <li>Hormonal Changes – Pregnant women are also at risk for developing the gallstones due to estrogen hormone increases cholesterol concentration in blood and bile.</li>
                    <li>Diet – Regular intake of a high-fat and high-cholesterol diet causes increased concentration of cholesterol in bile leading to gallstone formation.</li>
                    <li>Having a family history of gallstone.</li>
                    <li>High cholesterol level.</li>
                    <li>Medications – Proton Pump Inhibitor decreases contraction ability of gallbladder which causes increased concentration of cholesterol, bilirubin, and calcium carbonate.</li>
                </ul>

