            <p>Epistaxis is also called acute hemorrhage or nose bleed. It is a medical condition in which bleeding occurs from the nasal cavity of nostrils.
            This is a sign of increase in the body’s heat and is also caused due to increased blood pressure and some other systemic disorders. People usually suffer from this condition in the summers.</p>

            <p>most nosebleeds are benign, self-limiting and spontaneous. Bleeding from the nose usually occurs when the membranes lining the inner nose are disturbed or irritated and cause abnormal bleeding.</p>

            <p>Bleeding from the nose is a common complaint presenting an emergency. Epistaxis is often a frightening sight and can be due to some heart diseases, pregnancy, bleeding disorders and acute general conditions like typhoid, pneumonia, malaria, dengue fever, measles, etc.</p>
