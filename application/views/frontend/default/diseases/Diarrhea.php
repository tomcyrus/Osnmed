            <p>A Person is considered to have diarrhea if a person has loose stools three or more times a day. Diarrhea is a condition in which loose, watery stools (bowel movement) appear. Acute diarrhea is a condition that lasts a short time. It is a common problem that usually lasts about one or two days. Then it goes away on its own.</p>

            <p>A condition of diarrhea lasting more than a few days may be a sign of a more serious problem. Chronic diarrhea is a condition that lasts at least four weeks. In a condition of the weakened immune system, chronic diarrhea may represent a life-threatening illness.</p>
            <p>About 75 % of deaths are due to diarrhea occurring in the first two years of life. The main cause of death is from acute diarrhea that results from the loss of fluids and electrolytes in diarrheal stools. Other important causes of death are dysentery.</p>
            <p>Diarrhea is an important cause of under nutrition. This is because patients eat less during diarrhea and their ability to absorb nutrients is reduced.</p>
            
