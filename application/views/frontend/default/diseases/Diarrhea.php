            <h3 class="mb-4">What are the Causes of Diarrhea?</h3>
            <p>TAcute diarrhea is commonly caused by a bacterial (Salmonella), viral (rotavirus), or parasitic (Giardia) infection of the bowels. Diarrhea caused by any of these infections is called gastroenteritis.</p>
            <p>The most common condition of chronic diarrhea is following diseases and conditions:</p>
            <ul class="list-styled mb-5">

            <li>Bacteria born from contaminated food or water.</li>
            <li>Certain parasites, found in contaminated food or water.</li>
            <li>Use of certain medicines like antibiotics, cancer drugs, and antacids that contain magnesium.</li>
            <li>Gluten intolerance (e.g. Celiac disease)</li>
            <li>Lactose intolerance</li>
            <li>Irritable bowel syndrome</li>
            <li>Chronic bowel infection</li>
            </ul>


