          <br>
          <p><b>Introduction –</b></p>
          <br>
            <p>Acne is also termed as Acne vulgaris and is defined as a long term skin disease that occurs when hair follicles are clogged with oil and dead skin cells from the skin.
            It commonly occurs in adolescence and it was noticed that around 80-90% of teenagers suffered in the western world.</p>

            <p>While the condition is not life-threatening, it can cause significant psychological morbidity and lead to lifelong scarring. In some cases of facial disfigurement if left untreated.
            Acne generally starts around the age of 12 to 14 years but tends to manifest earlier in female patients. 
            The average age of severity is 16 to 17 years in females and 17 to 19 years in male patients.</p>
            <h3 class="mb-4">What are the Causes of Acne?</h3>

            <p>Generally, acne appears on the face, forehead, chest, upper back and shoulders because these areas of skin have the most oil (sebaceous) glands. The most common causes are –</p>
            <ul class="list-styled mb-5">
           <li> Excess production of oil</li>
            <li>Bacteria build up in pores</li>
           <li> Excess activity of a type of hormone</li>
           <li> Humidity or exposure to dust</li>
           <li> Stress/anxiety</li>
           <li> Hormonal changes in both men and women</li>
           <li> Genetics</li>
           <li> Accumulation of dead skin cells in pores</li>
           <li> Certain medications, such as certain birth control pills or corticosteroids</li>
           <li> Excessive intake of diet that is rich in refined sugars or carbohydrates, such as bread and chips</li>
            </ul>

            <h3 class="mb-4">What are the Symptoms of Acne?</h3>
            <p>While whiteheads and blackheads are the most common lesions seen in acne, but symptoms can vary from person to person. Inflammatory lesions are more likely to cause scarring of your skin. These include:</p>
            <ul class="list-styled mb-5">

            <li>Formation of papules – These are small, red, raised bumps caused by inflamed or infected hair follicles.</li>
            <li>Formation of pustules – These are small red pimples that have pus at their tips.</li>
            <li>Formation of nodules – Nodules is solid, often painful lumps beneath the surface of your skin.</li>
            <li>Formation of cyst – Cysts are large lumps found beneath your skin that contains pus and are usually painful.</li>
            </ul>

