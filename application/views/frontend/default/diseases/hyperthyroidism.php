            <p>Everyone needs a body that is disease free and healthy. Not only major diseases affect our bodies. But sometimes the glands present in our body makes a misbalance. Thyroid gland an endocrine gland present in the neck that contains two lobes connected by an isthmus. This gland produces hormones known as thyroxine/T4, triiodothyronine/T3, & calcitonin. When the thyroid gland in our body produces an excess of thyroxine hormone it is known as Hyperthyroidism. This disease is considered as a metabolic disorder that causes irregular heartbeat and unintentional weight loss.</p>
            <p>Hyperthyroidism is serious issue never ignore it as can lead to several other conditions. Women are more affected by this disease as compare to men.</p>
            
