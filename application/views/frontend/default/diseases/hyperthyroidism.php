            <h3 class="mb-4">What are the Causes of Hyperthyroidism?</h3>
            <p>There are different causes of Hyperthyroidism like thyroiditis, Plummer’s disease, & Grave’s disease. As the thyroid gland produces two main hormones thyroxine (T4) & triiodothyronine (T3) these two maintain the carbohydrates and fats, regulate the production of protein, controls body temperature, & the production of protein is regulated.
            The main reasons for too much production of thyroxine are Grave’s disease, stress, ovarian tumor, testis tumor, thyroid tumor, excess iodine intake, toxic multinodular goiter, & toxic adenoma, wrong eating habits ( intake of excessive processed and unhealthy food products), physical & emotional stress, etc.</p>

