            <p>Hepatic steatosis another name for fatty liver in which fats get accumulated in the liver. When there is a small amount of fats present in the liver than it is a normal health condition but too much accumulation of fats is considered as fatty liver. In human body, liver is the second largest organ that processes nutrients from food & drink that we consume.
            Its function is to filter harmful substances from the blood. There are mainly two types of fatty liver diseases one is known as an alcoholic fatty liver disease that develops when someone drinks a lot of alcohol. Another is a non-alcoholic fatty liver disease that is due to not drinking a lot of alcohol but including excessive fat in your diet. The wrong lifestyle is the main reason for it.</p>
