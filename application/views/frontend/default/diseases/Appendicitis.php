            <p>Appendicitis creates inflammation, pain, & swelling of the appendix, it’s a finger-like pouch connected to the large intestine. The exact cause of appendicitis is not yet known. In the case of appendicitis, usually a small piece of feces, blocks the entrance of the appendix, causing it to swell. As the inflammation worsens, appendicitis pain increases and eventually becomes very severe.
                The site of pain may vary, depending upon the age and position of the appendix present in the body. An Appendix is a small, thin pouch about five to ten centimeters long. It is also known as “vestigial organ” as it is of no use but still present in the body. An Appendix is connected to the large intestine in the human body.</p>
            <h3 class="mb-4">What are the signs and symptoms of appendicitis?</h3>

            <p>The different signs & symptoms that are seen in appendicitis patients are:</p>
            <ul class="list-styled mb-5">
           <li> Fever</li>
            <li>Vomiting</li>
           <li> Progressively worsening pain</li>
           <li> Nausea</li>
           <li> Loss of appetite</li>
           <li> Swelling in the abdomen</li>
           <li> Genetics</li>
           <li> An inability to pass gas</li>
            </ul>

            <h3 class="mb-4">What are the causes of appendicitis?</h3>
            <p>The different causes of appendicitis are mentioned below in detail:</p>
            <ul class="list-styled mb-5">

            <li>Inflammatory bowel disease</li>
            <li>Trauma to your abdomen</li>
            <li>Blockage of the appendix opening</li>
            <li> Cancer</li>
            <li>Parasites, stool and growths that can clog your appendiceal lumen</li>
            </ul>

            <h3 class="mb-4">What are the risk factors of appendicitis?</h3>

            <p>The risk factors that are responsible for appendicitis are mentioned below in details:</p>
            <ul class="list-styled mb-5">

            <li><b>Age-</b>Appendicitis most often affects people between the age of 14 and 30 years old.</li>
            <li><b>Family history- </b> People who have a family history of appendicitis are at higher risk of developing it.</li>
            <li><b> Sex- </b>Appendicitis is more common in males than in females.</li>
            </ul>

