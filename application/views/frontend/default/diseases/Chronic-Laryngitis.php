            <p >Laryngitis is known as inflammation of the larynx. Symptoms may include hoarse voice, fever, cough, pain in front of the neck. It is categorized as acute if it last less than three weeks and chronic if symptoms last more than three weeks.
            Acute cases occur as viral upper respiratory tract  infection. Chronic cases occur due to smoking, tuberculosis, allergies.</p>

            <h3 class="mb-4">What are the signs and symptoms of chronic laryngitis?</h3>
            <p>The signs & symptoms related to chronic laryngitis are:</p>

            <ul class="list-styled mb-5">
           <li> general muscle pain</li>
           <li> coughing</li>
           <li> changes in pitch and volume of voice</li>
           <li> hoarse voice</li>
            <li>dry or sore throat</li>
           <li>frequent throat clearing</li>
           <li> increased saliva production</li>
           <li> dysphagia</li>
           <li> the sensation of swelling in the larynx region</li>
           <li> fever</li>
            </ul>

            <h3 class="mb-4"> What are the causes of chronic laryngitis?</h3>

                <p>Chronic laryngitis can be infectious as well as non-infectious in the origin. It generally develops in response to either an infection, trauma to vocal folds or allergies.
                It may also be caused by more severe problems such as nerve damage, sores, polyps, hard, and thick lumps on the vocal cords. Acute laryngitis is caused due to bacterial, viral, fungal infection and trauma. Chronic laryngitis is caused by allergies, reflux, & auto-immune disorders.</p>

